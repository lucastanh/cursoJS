// não podemos criar constantes com palavras reservadas
// constantes precisam ter nomes significativos
// nomes de constantes não podem começar com números
// nomes de constantes não podem conter espaços ou traços
// nomes de constantes utilizam camelCase(exemploDeNomeParaUmaVariavel)
// constantes são case-sensitive
// não podemos modificar o valor de uma constante
// NÃO utilize var, UTILIZE const
const primeiroNumero = 5;
const segundoNumero = 10;
const soma = primeiroNumero + segundoNumero;
const somaDuplicada = soma * 2;

console.log(soma, somaDuplicada);

// 'typeof' mostra o tipo de dado
console.log(typeof(primeiroNumero));